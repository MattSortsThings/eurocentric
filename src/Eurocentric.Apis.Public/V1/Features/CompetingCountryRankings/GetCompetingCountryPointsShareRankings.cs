using CSharpFunctionalExtensions;
using Eurocentric.Apis.Public.V1.Config;
using Eurocentric.Apis.Public.V1.Dtos.CompetingCountryRankings;
using Eurocentric.Apis.Public.V1.Enums;
using Eurocentric.Components.EndpointMapping;
using Eurocentric.Components.Messaging;
using Eurocentric.Domain.Analytics.Rankings.CompetingCountries;
using Eurocentric.Domain.Core;
using JetBrains.Annotations;
using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Http.HttpResults;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Routing;
using SlimMessageBus;
using IResult = Microsoft.AspNetCore.Http.IResult;

namespace Eurocentric.Apis.Public.V1.Features.CompetingCountryRankings;

internal static class GetCompetingCountryPointsShareRankings
{
    private static async Task<IResult> ExecuteAsync(
        [AsParameters] GetCompetingCountryPointsShareRankingsRequest request,
        [FromServices] IRequestResponseBus bus,
        CancellationToken ct = default
    ) => await bus.DispatchAsync(request.ToQuery(), MapToOk, ct);

    private static Query ToQuery(this GetCompetingCountryPointsShareRankingsRequest request)
    {
        return new Query
        {
            MinYear = request.MinYear,
            MaxYear = request.MaxYear,
            ContestStage = request.ContestStage?.ToDomainContestStageFilter(),
            VotingCountryCode = request.VotingCountryCode,
            VotingMethod = request.VotingMethod?.ToDomainVotingMethodFilter(),
            PageIndex = request.PageIndex,
            PageSize = request.PageSize,
            Descending = request.Descending,
        };
    }

    private static Ok<GetCompetingCountryPointsShareRankingsResponse> MapToOk(PointsShareRankings rankings)
    {
        (List<PointsShareRanking> rankingRecords, PointsShareMetadata metadataRecord) = rankings;

        return TypedResults.Ok(
            new GetCompetingCountryPointsShareRankingsResponse(
                rankingRecords.Select(ranking => ranking.ToDto()).ToArray(),
                metadataRecord.ToDto()
            )
        );
    }

    internal sealed class EndpointMapper : IEndpointMapper
    {
        public void MapEndpoint(RouteGroupBuilder routeBuilder)
        {
            routeBuilder
                .MapGet("competing-country-rankings/points-share", ExecuteAsync)
                .WithName(V1EndpointNames.CompetingCountryRankings.GetCompetingCountryPointsShareRankings)
                .AddedInVersion1Point0()
                .WithSummary("Get competing country points share rankings")
                .WithDescription(
                    """
                    Ranks competing countries based on *descending points share*.

                    Returns a page of rankings and a metadata object.

                    Points share is a float between 0 and 1, calculated as the sum total points the competing country
                    received across broadcasts, as a fraction of the available points.

                    Rankings are generated by:

                    - Filtering voting data from query parameters.
                    - Grouping data by competing country.
                    - Calculating points share metric.
                    - Ranking by descending points share.
                    - Sorting rankings
                        - by rank (ascending or descending)
                        - then by country code (ascending).
                    - Applying pagination.
                    """
                )
                .WithTags(V1Tags.CompetingCountryRankings)
                .Produces<GetCompetingCountryPointsShareRankingsResponse>()
                .ProducesProblem(StatusCodes.Status400BadRequest)
                .ProducesProblem(StatusCodes.Status422UnprocessableEntity);
        }
    }

    internal sealed record Query : PointsShareQuery, IQuery<PointsShareRankings>;

    [UsedImplicitly]
    internal sealed class QueryHandler(ICompetingCountryRankingsGateway gateway)
        : IQueryHandler<Query, PointsShareRankings>
    {
        public async Task<Result<PointsShareRankings, IDomainError>> OnHandle(Query query, CancellationToken ct) =>
            await gateway.GetPointsShareRankingsAsync(query, ct);
    }
}
