using CSharpFunctionalExtensions;
using Eurocentric.Apis.Public.V1.Config;
using Eurocentric.Apis.Public.V1.Dtos.VotingCountryRankings;
using Eurocentric.Apis.Public.V1.Enums;
using Eurocentric.Components.EndpointMapping;
using Eurocentric.Components.Messaging;
using Eurocentric.Domain.Analytics.Rankings.VotingCountries;
using Eurocentric.Domain.Core;
using JetBrains.Annotations;
using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Http.HttpResults;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Routing;
using SlimMessageBus;
using IResult = Microsoft.AspNetCore.Http.IResult;

namespace Eurocentric.Apis.Public.V1.Features.VotingCountryRankings;

internal static class GetVotingCountryPointsAverageRankings
{
    private static async Task<IResult> ExecuteAsync(
        [AsParameters] GetVotingCountryPointsAverageRankingsRequest request,
        [FromServices] IRequestResponseBus bus,
        CancellationToken ct = default
    ) => await bus.DispatchAsync(request.ToQuery(), MapToOk, ct);

    private static Query ToQuery(this GetVotingCountryPointsAverageRankingsRequest request)
    {
        return new Query
        {
            CompetingCountryCode = request.CompetingCountryCode,
            MinYear = request.MinYear,
            MaxYear = request.MaxYear,
            ContestStage = request.ContestStage?.ToDomainContestStageFilter(),
            VotingMethod = request.VotingMethod?.ToDomainVotingMethodFilter(),
            PageIndex = request.PageIndex,
            PageSize = request.PageSize,
            Descending = request.Descending,
        };
    }

    private static Ok<GetVotingCountryPointsAverageRankingsResponse> MapToOk(PointsAverageRankings rankings)
    {
        (List<PointsAverageRanking> rankingRecords, PointsAverageMetadata metadataRecord) = rankings;

        return TypedResults.Ok(
            new GetVotingCountryPointsAverageRankingsResponse(
                rankingRecords.Select(ranking => ranking.ToDto()).ToArray(),
                metadataRecord.ToDto()
            )
        );
    }

    internal sealed class EndpointMapper : IEndpointMapper
    {
        public void MapEndpoint(RouteGroupBuilder routeBuilder)
        {
            routeBuilder
                .MapGet("voting-country-rankings/points-average", ExecuteAsync)
                .WithName(V1EndpointNames.VotingCountryRankings.GetVotingCountryPointsAverageRankings)
                .AddedInVersion1Point0()
                .WithSummary("Get voting country points average rankings")
                .WithDescription(
                    """
                    Ranks voting countries based on *descending points average*.

                    Returns a page of rankings and a metadata object.

                    Points average is a float between 0 and 12, calculated as the average individual points value
                    the voting country gave to a specified competing country across broadcasts.

                    Rankings are generated by:

                    - Filtering voting data from query parameters.
                    - Grouping data by voting country.
                    - Calculating points average metric.
                    - Ranking by descending points average.
                    - Sorting rankings
                        - by rank (ascending or descending)
                        - then by country code (ascending).
                    - Applying pagination.
                    """
                )
                .WithTags(V1Tags.VotingCountryRankings)
                .Produces<GetVotingCountryPointsAverageRankingsResponse>()
                .ProducesProblem(StatusCodes.Status400BadRequest)
                .ProducesProblem(StatusCodes.Status422UnprocessableEntity);
        }
    }

    internal sealed record Query : PointsAverageQuery, IQuery<PointsAverageRankings>;

    [UsedImplicitly]
    internal sealed class QueryHandler(IVotingCountryRankingsGateway gateway)
        : IQueryHandler<Query, PointsAverageRankings>
    {
        public async Task<Result<PointsAverageRankings, IDomainError>> OnHandle(Query query, CancellationToken ct) =>
            await gateway.GetPointsAverageRankingsAsync(query, ct);
    }
}
